const GiB = 1024 ** 3;

const SMB_CONF = process.env.SMB_CONF ?? "/etc/samba/smb.conf.d/shares.conf";
const NFS_EXPORTS = process.env.NFS_EXPORTS ?? "/etc/exports.d/nas.exports";

// Validation charsets (no shell metacharacters).
const NAME_RE = /^[A-Za-z0-9][A-Za-z0-9_\- ]*$/;
const PATH_RE = /^[A-Za-z0-9._\-/]+$/;

interface State {
  folders: SharedFolder[];
  services: FileServices;
}

