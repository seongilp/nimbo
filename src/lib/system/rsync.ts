function notifyJob(job: RsyncJob) {
  if (job.lastStatus === "failed") {
    void emitEvent("backup.failed", "critical", `백업 실패: ${job.name}`, job.lastError ?? "rsync 작업이 실패했습니다.");
  } else if (job.lastStatus === "success") {
    void emitEvent("backup.success", "info", `백업 완료: ${job.name}`, `${formatBytesShort(job.lastBytes)} · ${job.lastFiles}개 파일 전송`);
  }
}

function formatBytesShort(b: number): string {
  if (b > 1024 ** 3) return (b / 1024 ** 3).toFixed(1) + " GB";
  if (b > 1024 ** 2) return (b / 1024 ** 2).toFixed(0) + " MB";
  return (b / 1024).toFixed(0) + " KB";
}

const GiB = 1024 ** 3;
const RSYNC_CONF = process.env.RSYNC_CONF ?? "/etc/rsyncd.conf";

const SCHED_MS: Record<Exclude<SyncSchedule, "manual">, number> = {
  hourly: 3600_000,
  daily: 86_400_000,
  weekly: 604_800_000,
};

// Safe-ish charset for rsync remote/local endpoints (no shell metacharacters).
const ENDPOINT_RE = /^[A-Za-z0-9@._:\-/~]+$/;
const NAME_RE = /^[A-Za-z0-9][A-Za-z0-9_\- ]*$/;

